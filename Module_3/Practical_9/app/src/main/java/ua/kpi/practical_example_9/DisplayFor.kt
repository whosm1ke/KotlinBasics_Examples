package ua.kpi.practical_example_9

/**
 * Enum-клас, що визначає рівні відображення (display) для Practical Example 9.
 * Використовується для позначення рівня складності або деталізації відображення даних.
 */
enum class DisplayFor {
    /**
     * Базовий рівень відображення.
     * Відповідає мінімальному набору інформації для відображення.
     */
    BASIC_LEVEL,
    
    /**
     * Середній рівень відображення.
     * Включає більше деталей порівняно з базовим рівнем.
     */
    MIDDLE_LEVEL,
    
    /**
     * Просунутий рівень відображення.
     * Відповідає максимальному набору інформації для детального відображення.
     */
    ADVANCED_LEVEL,
}