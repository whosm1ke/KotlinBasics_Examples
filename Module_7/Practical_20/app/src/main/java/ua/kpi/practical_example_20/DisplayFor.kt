package ua.kpi.practical_example_20

/**
 * Enum-клас, який визначає рівні відображення (display) для практичного прикладу.
 * Кожен елемент enum представляє різний рівень складності або функціональності,
 * який може бути використаний для налаштування відображення інформації.
 */
enum class DisplayFor {
    /**
     * Базовий рівень відображення.
     * Використовується для простих, мінімальних відображень інформації.
     */
    BASIC_LEVEL,
    
    /**
     * Середній рівень відображення.
     * Використовується для більш детального відображення з додатковою інформацією.
     */
    MIDDLE_LEVEL,
    
    /**
     * Розширений рівень відображення.
     * Використовується для повного, детального відображення з усіма доступними даними.
     */
    ADVANCED_LEVEL,
}