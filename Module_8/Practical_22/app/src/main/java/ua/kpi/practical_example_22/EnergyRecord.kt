package ua.kpi.practical_example_22

// Модель даних для одного запису про енергоспоживання
// Використовується для зберігання інформації про вимірювання потужності електроенергії
// на певну дату з вказаним статусом станції

data class EnergyRecord(
    val date: String,      // Дата вимірювання у форматі рядка (наприклад, "2023-10-01")
    val power: Double,      // Потужність у кіловатт (кВт), числове значення
    val status: String = "Активна"  // Статус станції, за замовчуванням "Активна"
    // Можливі значення: "Активна", "Помилка", "Тех. обслуговування" тощо
)