package ua.kpi.practical_example_11

/**
 * Enum-клас, що визначає рівні відображення (display) для різних рівнів складності.
 * Використовується для позначення того, який рівень інформації має бути показаний користувачеві.
 */
enum class DisplayFor {
    /**
     * Базовий рівень відображення. 
     * Відповідає мінімальному набору інформації, що потрібен для початкового розуміння.
     */
    BASIC_LEVEL,
    
    /**
     * Середній рівень відображення. 
     * Включає більше деталей, ніж базовий рівень, але менше, ніж продвинутий.
     */
    MIDDLE_LEVEL,
    
    /**
     * Продвинутий рівень відображення. 
     * Відображає повну інформацію, включаючи технічні деталі та розширені параметри.
     */
    ADVANCED_LEVEL,
}