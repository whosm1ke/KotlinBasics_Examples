package ua.kpi.practical_example_26

/**
 * Enum-клас, що визначає рівні відображення (display) для користувачів.
 * Використовується для позначення рівня доступу або складності інтерфейсу,
 * який показується користувачеві в залежності від його досвіду або ролі.
 *
 * Можливі значення:
 * - BASIC_LEVEL: базовий рівень, найпростіший інтерфейс
 * - MIDDLE_LEVEL: середній рівень, з більшою кількістю функцій
 * - ADVANCED_LEVEL: продвинутий рівень, для досвідчених користувачів
 */
enum class DisplayFor {
    BASIC_LEVEL,     // Базовий рівень відображення
    MIDDLE_LEVEL,    // Середній рівень відображення
    ADVANCED_LEVEL,  // Продвинутий рівень відображення
}