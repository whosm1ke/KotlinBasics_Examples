package ua.kpi.practical_example_5

/**
 * Enum-клас, який визначає рівні відображення (display) для практичного прикладу №5.
 * Використовується для позначення рівня складності або деталізації відображення даних.
 */
enum class DisplayFor {
    /**
     * Базовий рівень відображення.
     * Відповідає мінімальному набору інформації.
     */
    BASIC_LEVEL,
    
    /**
     * Середній рівень відображення.
     * Включає більше деталей порівняно з базовим рівнем.
     */
    MIDDLE_LEVEL,
    
    /**
     * Розширений рівень відображення.
     * Найбільш детальний рівень, що містить максимальну кількість інформації.
     */
    ADVANCED_LEVEL,
}