package ua.kpi.practical_example_14

/**
 * Enum-клас, який визначає рівні відображення (display) для різних рівнів складності.
 * Використовується для позначення того, на якому рівні повинна відображатися інформація
 * у залежності від потреб користувача або складності завдання.
 */
enum class DisplayFor {
    /**
     * Базовий рівень відображення — найпростіший рівень, який відповідає
     * мінімальним потребам користувача або початковому рівню складності.
     */
    BASIC_LEVEL,

    /**
     * Середній рівень відображення — відповідає середньому рівню складності,
     * де відображається більше інформації, ніж на базовому рівні.
     */
    MIDDLE_LEVEL,

    /**
     * Розширений рівень відображення — найвищий рівень, який відображає
     * повну інформацію та деталі, необхідні для складних завдань.
     */
    ADVANCED_LEVEL,
}