package ua.kpi.practical_example_2.data

class Calculator {
    /**
     * Обчислює потужність сонячної панелі з урахуванням радіації, ККД та площі панелі.
     * 
     * @param radiation Рівень сонячної радіації (в Вт/м²), може бути рядком для переведення в число.
     * @param efficiency ККД сонячної панелі у відсотках, може бути рядком для переведення в число.
     * @param area Площа сонячної панелі (в м²), може бути рядком для переведення в число.
     * @return Обчислена потужність у ватах (Вт).
     */
    fun calculatePowerAdvanced(radiation: String, efficiency: String, area: String): Double {
        // Перетворюємо радіацію з рядка в число, якщо не вдається — використовуємо 0.0
        val rad = radiation.toDoubleOrNull() ?: 0.0
        
        // Перетворюємо ККД з рядка в число і переводимо у відсотки (ділимо на 100), якщо не вдається — використовуємо 0.0
        val eff = efficiency.toDoubleOrNull()?.div(100) ?: 0.0
        
        // Перетворюємо площу з рядка в число, якщо не вдається — використовуємо 0.0
        val a = area.toDoubleOrNull() ?: 0.0
        
        // Обчислення потужності: P = радіація × ККД × площа
        return rad * eff * a
    }
}